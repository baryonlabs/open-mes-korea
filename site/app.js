const languageSelect = document.querySelector("#language");

function setLanguage(language) {
  const dictionary = translations[language] || translations.ko;
  const htmlLanguages = { zh: "zh-CN", pt: "pt-BR" };
  document.documentElement.lang = htmlLanguages[language] || language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = dictionary[element.dataset.i18n];
    if (value) element.innerHTML = value;
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = dictionary[element.dataset.i18nAlt];
    if (value) element.alt = value;
  });

  languageSelect.value = language;
  localStorage.setItem("open-mes-language", language);
}

const savedLanguage = localStorage.getItem("open-mes-language");
const browserLanguage = navigator.language.toLowerCase().split("-")[0];
const initialLanguage = savedLanguage || (translations[browserLanguage] ? browserLanguage : "ko");

languageSelect.addEventListener("change", (event) => setLanguage(event.target.value));
setLanguage(initialLanguage);

